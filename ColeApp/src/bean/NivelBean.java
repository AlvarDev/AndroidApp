package bean;

public class NivelBean {

	int idNivel;
	String descripcion;
	
	public NivelBean() {
		idNivel=0;
		descripcion="";
	}

	public int getIdNivel() {
		return idNivel;
	}

	public void setIdNivel(int idNivel) {
		this.idNivel = idNivel;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
	
	
}
